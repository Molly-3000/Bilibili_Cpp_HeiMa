#include<iostream>
using namespace std;

int main18() {

	// И│охн╦╦сич
	// =
	int a = 10;
	a = 100;
	cout << "a = " << a << endl;


	// +=
	a = 10;
	a += 20; // a = a + 20;
	cout << "a = " << a << endl;

	// -= 
	a = 10;
	a -= 20; // a = a - 20;
	cout << "a = " << a << endl;

	// *=
	a = 10;
	a *= 0.2; // a = a * 0.2;
	cout << "a = " << a << endl;

	// /=
	a = 10;
	a /= 3.0f; // a = a / 3.0f;
	cout << "a = " << a << endl;

	// %=
	a = 10;
	a %= 3; // a = a % 3;
	cout << "a = " << a << endl;

	system("pause");
	return 0;
}